var x=20;
console.log(x);  //20
console.log(x++);  //21 but 20 (actually it is increased but while printing it will show 20 only)
x++;   //22
console.log(x);   //22
console.log(++x);   //23
++x;    //24
console.log(x);   //24

x--;     //23
console.log(x--);   //22 but 23
console.log(x);     //22
--x;    //21
console.log(--x);   //20
console.log(x);    //20


